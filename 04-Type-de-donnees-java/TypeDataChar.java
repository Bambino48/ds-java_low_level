// Type de données Java char :
/*
 * le type de données char est un seul caractère Unicode de 16 bits
 * La valeur minimale est '\u0000' (ou 0)
 * La valeur maximale est '\uffff' (ou 65 535 inclus)
 * Le type de données Char est utilisé pour stocker n'importe quel caractère
 * Exemple : char letterA = 'A'
 */

public class TypeDataChar {

    public static void main(String[] args) {

        char charValeur = 'A';

        System.out.println("Le caractere char est : " + charValeur);
    }

}
