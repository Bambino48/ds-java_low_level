// 08-Commentaires-en-java
/*
 * Les commentaires Java sont des notes textuelles écrites dans le code
 * pour fournir une explication sur le code source.
 * Les commentaires peuvent être utilisés pour expliquer la logique ou à des
 * fins de documentation.
 * Le compilateur ne compile pas les commentaires.
 * En Java, les commentaires sont très similaires à ceux du C et du C++ .
 */
/*
 * En Java, il existe trois types de commentaires :
 * 
 * Commentaires sur une seule ligne
 * Commentaires multilignes
 * Commentaires sur la documentation
 * 
 */