// Conversion de type Java (conversion de type)
/*
 * Le transtypage est une technique utilisée soit par le compilateur,
 * soit par un programmeur pour convertir un type de données en un autre.
 * Par exemple, convertir int en double, double en int, short en int, etc.
 * La saisie de type est également connue sous le nom de conversion de type.
 * 
 * Il existe deux types de typage cast autorisés dans la programmation Java :
 * 
 * Moulage de type élargissant
 * Moulage de type rétrécissant
 * 
 */