/*
 * Objet : Les objets ont des états et des comportements. 
 * Exemple : Un chien a des états - couleur, nom, race ainsi que 
 * des comportements tels que remuer la queue, aboyer, manger. 
 * Un objet est une instance d'une classe.
 */
/*
 * Classe : Une classe peut être définie comme un modèle/plan directeur 
 * qui décrit le comportement/l'état pris en charge par l'objet de son type.
 */
/*
 * Méthodes : Une méthode est fondamentalement un comportement. 
 * Une classe peut contenir plusieurs méthodes. 
 * C'est dans les méthodes que la logique est écrite, 
 * les données sont manipulées et toutes les actions sont exécutées.
 */
/*
 * Variables d'instance : Chaque objet a son ensemble unique de variables d'instance. 
 * L'état d'un objet est créé par les valeurs attribuées à ces variables d'instance.
 */
/*
 * Les objets, classes, méthodes et variables d'instance sont des mots pour
 * la programmation orienté objets(POO)
 */

